#include "libft.h"

void			ft_putnbr(int n)
{
	long z;

	z = n;
	if (n < 0)
	{
		z = -z;
		ft_putchar('-');
	}
	if (z > 9)
	{
		ft_putnbr(z / 10);
		ft_putnbr(z % 10);
	}
	else
		ft_putchar(z % 10 + '0');
}
